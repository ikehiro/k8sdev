package com.msa.customer.presentation;

import com.msa.customer.infrastructure.entity.CustomerEntity;
import com.msa.customer.infrastructure.repository.CustomerEntityRepository;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Controller;
import org.springframework.web.bind.annotation.*;

@Controller    // This means that this class is a Controller
@RequestMapping(path = "/demo") // This means URL's start with /demo (after Application path)
public class MainController {
    @Autowired // This means to get the bean called userRepository
    // Which is auto-generated by Spring, we will use it to handle the data
    private CustomerEntityRepository customerEntityRepository;

    @PostMapping(path = "/add") // Map ONLY POST Requests
    public @ResponseBody
    String addNewCustomer(@RequestParam String name
            , @RequestParam String corporateNumber
            , @RequestParam String jsonVal) {
        // @ResponseBody means the returned String is the response, not a view name
        // @RequestParam means it is a parameter from the GET or POST request

        CustomerEntity n = new CustomerEntity();
        n.setName(name);
        n.setCorporateNumber(corporateNumber);
        n.setJsonVal(jsonVal);
        //n.setEmail(email);
        customerEntityRepository.save(n);
        return "Saved";
    }

    @GetMapping(path = "/all")
    public @ResponseBody
    Iterable<CustomerEntity> getAllCustomers() {
        // This returns a JSON or XML with the users
        return customerEntityRepository.findAll();
    }
}
